function changeLanguage(lang) {
    const texts = translations[lang] || translations['en'];
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (texts[key]) {
            // Handle HTML content safely if needed, or just text
            if (element.tagName === 'SPAN' && element.classList.contains('bg-clip-text')) {
                 // Special case for hero highlight to preserve classes, but here we just replace textContent
                 element.textContent = texts[key];
            } else {
                 element.textContent = texts[key];
            }
        }
    });

    // Special handling for HTML structures (Hero Headline)
    const hl1 = document.querySelector('[data-i18n="hero_headline_1"]');
    const hlH = document.querySelector('[data-i18n="hero_headline_highlight"]');
    const hl2 = document.querySelector('[data-i18n="hero_headline_2"]');
    
    if (hl1 && texts["hero_headline_1"]) hl1.textContent = texts["hero_headline_1"];
    if (hlH && texts["hero_headline_highlight"]) hlH.textContent = texts["hero_headline_highlight"];
    if (hl2 && texts["hero_headline_2"]) hl2.textContent = texts["hero_headline_2"];

    // Update URL without reload (optional, for aesthetics)
    // history.pushState({}, '', '?lang=' + lang);
    localStorage.setItem('ultimatepdf_lang', lang);
}

// Auto-detect language on load
document.addEventListener('DOMContentLoaded', () => {
    const urlParams = new URLSearchParams(window.location.search);
    const langParam = urlParams.get('lang');
    const savedLang = localStorage.getItem('ultimatepdf_lang');
    const browserLang = navigator.language.slice(0, 2);
    
    let targetLang = 'en';
    
    if (langParam && translations[langParam]) {
        targetLang = langParam;
    } else if (savedLang && translations[savedLang]) {
        targetLang = savedLang;
    } else if (translations[browserLang]) {
        targetLang = browserLang;
    }
    
    changeLanguage(targetLang);
});
